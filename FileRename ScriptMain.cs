		public void Main()
        {
           
		   string SourceFolder;
           string DestinationFolder;
           DateTime LastLoadDate;
           int dateCompare;
          
            //Load the internal varaibles with the SSIS variables
           SourceFolder = Dts.Variables["SourceFolder"].Value.ToString();
           DestinationFolder = Dts.Variables["DestinationFolder"].Value.ToString();         
           LastLoadDate = (DateTime)Dts.Variables["LastLoadDate"].Value;          
  
            //String array to store the file names in the source folder.
            string[] files = Directory.GetFiles(SourceFolder); 
                       
            foreach (string file in files) //Traverse through the files in the folder
            {
                FileInfo fileinfo = new FileInfo(file);
                dateCompare = DateTime.Compare(fileinfo.LastWriteTime,LastLoadDate); //Compare if the Modified date is more than the last load
            
                if(dateCompare >= 0) //Found new modified files.
                {

                    try
                    {
                        string filename = fileinfo.Name.ToString(); //Get the filename
                        string DestFilename = (fileinfo.Name.Replace(fileinfo.Extension, "").ToString() + "_" + DateTime.Now.ToString("yyyyMMdd_HHmm")) + fileinfo.Extension; //Get the filename
                        File.Copy(Path.Combine(SourceFolder, filename), Path.Combine(DestinationFolder, DestFilename), true); //Copy the file to the SSIS directory and overwrite
                        Dts.TaskResult = (int)ScriptResults.Success;
                    }

                    catch (IOException copyError)
                    {
                        Dts.Events.FireError(0, "Copy of files failed", copyError.Message + "\r" + copyError.StackTrace, String.Empty, 0);
                                        
                    }
                }
            }

		}

